package va3;
public class Aluno {
    private String nome;
    private int idadde;
    

    public Aluno(String nome, int idadde) {
        this.nome = nome;
        this.idadde = idadde;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdadde() {
        return idadde;
    }

    public void setIdadde(int idadde) {
        this.idadde = idadde;
    }

   
    
    
}
